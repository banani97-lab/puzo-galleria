export default function Product() {
  const {product} = useLoaderData<typeof loader>();

  // Optimistically selects a variant with given available variant information
  const selectedVariant = useOptimisticVariant(
    product.selectedOrFirstAvailableVariant,
    getAdjacentAndFirstAvailableVariants(product),
  );

  // Sets the search param to the selected variant without navigation
  // only when no search params are set in the url
  useSelectedOptionInUrlParam(selectedVariant.selectedOptions);

  // Get the product options array
  const productOptions = getProductOptions({
    ...product,
    selectedOrFirstAvailableVariant: selectedVariant,
  });

  const {title, descriptionHtml} = product;

  return (
    <div className="product">
      <ProductImage image={selectedVariant?.image} />
      <div style={{ fontFamily: 'Bodoni'}} className="product-main">
        <h1>{title}</h1>
        <ProductPrice
          price={selectedVariant?.price}
          compareAtPrice={selectedVariant?.compareAtPrice}
        />
        <h5 style={{ fontFamily: 'Bodoni'}}>Tax Included</h5>
        <br />
        <ProductForm
          productOptions={productOptions}
          selectedVariant={selectedVariant}
        />
        <br />
        <br />
        <br />
        <CartDrawer description={product.description} />
      </div>
      <Analytics.ProductView
        data={{
          products: [
            {
              id: product.id,
              title: product.title,
              price: selectedVariant?.price.amount || '0',
              vendor: product.vendor,
              variantId: selectedVariant?.id || '',
              variantTitle: selectedVariant?.title || '',
              quantity: 1,
            },
          ],
        }}
      />
    </div>
  );
}

type CartDrawerProps = {
  description: string
}

function CartDrawer({ description}: CartDrawerProps) {
  const [expandedSection, setExpandedSection] = useState<string | null>(null);

  const toggleSection = (section: string) => {
    setExpandedSection(expandedSection === section ? null : section);
  };

  return (
    <div className="cart-drawer w-full bg-white" style={{ fontFamily: 'Bodoni'}}>
      {[
        {
          title: 'Description',
          content: (
            <>
            { description }
            </>
          )
        },
        {
          title: 'Delivery Estimate',
          content: (
            <>
              Denmark: 1-2 Business Days
              <br />
              Rest of Europe: 2-7 Business Days
            </>
          ),
        },
        {
          title: 'Returns',
          content: (
            <>
              Denmark: 1-2 Business Days
              <br />
              EU: 2-7 Business Days
            </>
          ),
        },
        {
          title: 'Contact Us',
          content: 'Feel free to contact us at contact@puzo.com',
        },
      ].map(({ title, content }) => (
        <div key={title} className="mb-4 border-b border-gray-300 pb-2">
          <button
            style={{ fontFamily: 'Bodoni' }}
            className="flex justify-between items-center w-full text-left p-2 font-semibold"
            onClick={() => toggleSection(title)}
          >
            {title}
            <img
              src="/assets/plus.png"
              alt="Toggle"
              className={`w-5 h-5 transition-transform duration-300 ${
                expandedSection === title ? 'rotate-45' : 'rotate-0'
              }`}
            />
          </button>
          <div
            className={`pl-2 overflow-hidden transition-max-h duration-300 ${
              expandedSection === title ? 'max-h-40' : 'max-h-0'
            }`}
          >
            <p
              style={{ fontFamily: 'Bodoni' }}
              className="p-3 text-gray-700"
            >
              {content}
            </p>
          </div>
        </div>
      ))}
    </div>
  );
}

const PRODUCT_VARIANT_FRAGMENT = `#graphql
  fragment ProductVariant on ProductVariant {
    availableForSale
    compareAtPrice {
      amount
      currencyCode
    }
    id
    image {
      __typename
      id
      url
      altText
      width
      height
    }
    price {
      amount
      currencyCode
    }
    product {
      title
      handle
    }
    selectedOptions {
      name
      value
    }
    sku
    title
    unitPrice {
      amount
      currencyCode
    }
  }
` as const;

const PRODUCT_FRAGMENT = `#graphql
  fragment Product on Product {
    id
    title
    vendor
    handle
    descriptionHtml
    description
    encodedVariantExistence
    encodedVariantAvailability
    options {
      name
      optionValues {
        name
        firstSelectableVariant {
          ...ProductVariant
        }
        swatch {
          color
          image {
            previewImage {
              url
            }
          }
        }
      }
    }
    selectedOrFirstAvailableVariant(selectedOptions: $selectedOptions, ignoreUnknownOptions: true, caseInsensitiveMatch: true) {
      ...ProductVariant
    }
    adjacentVariants (selectedOptions: $selectedOptions) {
      ...ProductVariant
    }
    seo {
      description
      title
    }
  }
  ${PRODUCT_VARIANT_FRAGMENT}
` as const;

const PRODUCT_QUERY = `#graphql
  query Product(
    $country: CountryCode
    $handle: String!
    $language: LanguageCode
    $selectedOptions: [SelectedOptionInput!]!
  ) @inContext(country: $country, language: $language) {
    product(handle: $handle) {
      ...Product
    }
  }
  ${PRODUCT_FRAGMENT}
` as const;
